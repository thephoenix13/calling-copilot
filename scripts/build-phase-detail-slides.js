const pptx = new PptxGenJS();
pptx.defineLayout({ name: 'W16x9', width: 13.333, height: 7.5 });
pptx.layout = 'W16x9';
pptx.author = 'Zeople';
pptx.title = 'Zeople RecruiterOS — RPO Phase Detail';

const W = 13.333;
const MX = 0.32;            // side margin
const USABLE = W - MX * 2;

PHASES.forEach((ph, idx) => {
  const s = pptx.addSlide();
  s.background = { color: BG };

  // top accent bar + dark footer
  s.addShape(pptx.ShapeType.rect, { x: 0, y: 0, w: W, h: 0.06, fill: { color: ph.color } });
  s.addShape(pptx.ShapeType.rect, { x: 0, y: 7.13, w: W, h: 0.37, fill: { color: FOOTER } });
  s.addText(FOOTER_TXT, { x: MX, y: 7.13, w: USABLE, h: 0.37, align: 'center', valign: 'middle',
    fontFace: FONT, fontSize: 7, color: LIGHT });

  // header: eyebrow + tagline + phase index
  s.addText(ph.name, { x: MX, y: 0.18, w: 9, h: 0.26, fontFace: FONT, fontSize: 11, bold: true, color: ph.color });
  s.addText(`PHASE ${idx + 1} OF 5`, { x: W - MX - 3, y: 0.18, w: 3, h: 0.26, align: 'right',
    fontFace: FONT, fontSize: 8, color: LIGHT });
  s.addText(ph.tagline, { x: MX, y: 0.46, w: USABLE, h: 0.5, fontFace: FONT, fontSize: 20, bold: true, color: NAVY });
  s.addShape(pptx.ShapeType.rect, { x: MX, y: 1.16, w: USABLE, h: 0.018, fill: { color: BORDER } });

  // cards
  const n = ph.agents.length;
  const gap = n >= 5 ? 0.18 : n === 4 ? 0.22 : 0.28;
  const cw = (USABLE - gap * (n - 1)) / n;
  const top = 1.36, cardH = 5.66, hdrH = 0.62;
  const bodyFs = n >= 5 ? 8 : n === 4 ? 8.5 : 9.5;
  const hdrNameFs = n >= 5 ? 11 : n === 4 ? 12 : 13;

  ph.agents.forEach((a, i) => {
    const x = MX + i * (cw + gap);
    // card body (white with border) + colored header strip
    s.addShape(pptx.ShapeType.rect, { x, y: top, w: cw, h: cardH, fill: { color: CARD },
      line: { color: BORDER, width: 1 } });
    s.addShape(pptx.ShapeType.rect, { x, y: top, w: cw, h: hdrH, fill: { color: ph.color } });
    s.addText(
      [{ text: a.n, options: { fontSize: hdrNameFs + 3, bold: true, breakLine: true } },
       { text: a.name, options: { fontSize: hdrNameFs, bold: true } }],
      { x: x + 0.12, y: top, w: cw - 0.2, h: hdrH, valign: 'middle', align: 'left',
        fontFace: FONT, color: 'FFFFFF', lineSpacingMultiple: 0.95 });

    // body: Purpose / Outcome / Technical with colored labels
    const block = (label, body, last) => ([
      { text: label, options: { fontSize: bodyFs - 0.5, bold: true, color: ph.color, breakLine: true, charSpacing: 1 } },
      { text: body, options: { fontSize: bodyFs, color: NAVY, breakLine: true } },
      ...(last ? [] : [{ text: ' ', options: { fontSize: 4, breakLine: true } }]),
    ]);
    s.addText(
      [...block('PURPOSE', a.purpose), ...block('OUTCOME', a.outcome), ...block('TECHNICAL', a.tech, true)],
      { x: x + 0.14, y: top + hdrH + 0.12, w: cw - 0.28, h: cardH - hdrH - 0.24,
        valign: 'top', align: 'left', fontFace: FONT, lineSpacingMultiple: 1.0, fit: 'shrink' });
  });
});

